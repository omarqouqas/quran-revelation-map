/** Get surahs filtered by revelation year */
export const getSurahsByYear = (year: number): Surah[] => {
  return surahs.filter(surah => surah.approximateYear <= year);
};

/** Get surahs by classification */
export const getSurahsByClassification = (classification: 'Makki' | 'Madani'): Surah[] => {
  return surahs.filter(surah => surah.classification === classification);
};

/** Get a surah by its number */
export const getSurahByNumber = (number: number): Surah | undefined => {
  return surahs.find(surah => surah.number === number);
};

/** Get surahs sorted by revelation order */
export const getSurahsByRevelationOrder = (): Surah[] => {
  return [...surahs].sort((a, b) => a.revelationOrder - b.revelationOrder);
};
